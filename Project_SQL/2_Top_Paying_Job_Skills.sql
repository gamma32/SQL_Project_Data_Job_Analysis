With TopPayingJobs as (
    select f.job_id, f.job_title 
        , f.salary_year_avg 
        , c.name as Company_Name
    from job_postings_fact f
        left join company_dim c on f.company_id = c.company_id
    where f.job_title_short = 'Data Analyst' and f.job_location='Anywhere'
        and f.salary_year_avg is not null
    order by f.salary_year_avg desc 
    limit 10
    )
select TPJ.* 
    , SD.skills, SD.type
from TopPayingJobs TPJ 
    inner join skills_job_dim SJD   using (job_id) -- on TPJ.job_id=SJD.job_id
    inner join skills_dim SD        using (skill_id) -- on SJD.skill_id=SD.skill_id
order by TPJ.salary_year_avg desc
;
